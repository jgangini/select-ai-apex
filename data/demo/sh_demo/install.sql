CREATE TABLE SH_DEMO.COUNTRIES (
  COUNTRY_ID CHAR(2) CONSTRAINT SH_DEMO_COUNTRIES_PK PRIMARY KEY,
  COUNTRY_NAME VARCHAR2(80) NOT NULL,
  REGION_NAME VARCHAR2(80) NOT NULL
);

CREATE TABLE SH_DEMO.CHANNELS (
  CHANNEL_ID NUMBER CONSTRAINT SH_DEMO_CHANNELS_PK PRIMARY KEY,
  CHANNEL_DESC VARCHAR2(40) NOT NULL,
  CHANNEL_CLASS VARCHAR2(40) NOT NULL
);

CREATE TABLE SH_DEMO.PRODUCTS (
  PROD_ID NUMBER CONSTRAINT SH_DEMO_PRODUCTS_PK PRIMARY KEY,
  PROD_NAME VARCHAR2(80) NOT NULL,
  PROD_CATEGORY VARCHAR2(50) NOT NULL,
  PROD_SUBCATEGORY VARCHAR2(50) NOT NULL,
  PROD_LIST_PRICE NUMBER(10,2) NOT NULL
);

CREATE TABLE SH_DEMO.CUSTOMERS (
  CUST_ID NUMBER CONSTRAINT SH_DEMO_CUSTOMERS_PK PRIMARY KEY,
  CUST_FIRST_NAME VARCHAR2(40) NOT NULL,
  CUST_LAST_NAME VARCHAR2(40) NOT NULL,
  CUST_GENDER CHAR(1),
  CUST_CITY VARCHAR2(60),
  CUST_STATE_PROVINCE VARCHAR2(60),
  COUNTRY_ID CHAR(2) NOT NULL,
  CUST_INCOME_LEVEL VARCHAR2(40),
  CUST_CREDIT_LIMIT NUMBER(12,2),
  CONSTRAINT SH_DEMO_CUSTOMERS_COUNTRY_FK FOREIGN KEY (COUNTRY_ID) REFERENCES SH_DEMO.COUNTRIES (COUNTRY_ID)
);

CREATE TABLE SH_DEMO.TIMES (
  TIME_ID DATE CONSTRAINT SH_DEMO_TIMES_PK PRIMARY KEY,
  CALENDAR_YEAR NUMBER(4) NOT NULL,
  CALENDAR_QUARTER_DESC VARCHAR2(20) NOT NULL,
  CALENDAR_MONTH_DESC VARCHAR2(20) NOT NULL,
  DAY_NAME VARCHAR2(20) NOT NULL
);

CREATE TABLE SH_DEMO.PROMOTIONS (
  PROMO_ID NUMBER CONSTRAINT SH_DEMO_PROMOTIONS_PK PRIMARY KEY,
  PROMO_NAME VARCHAR2(80) NOT NULL,
  PROMO_CATEGORY VARCHAR2(40) NOT NULL,
  PROMO_COST NUMBER(10,2) NOT NULL
);

CREATE TABLE SH_DEMO.SALES (
  PROD_ID NUMBER NOT NULL,
  CUST_ID NUMBER NOT NULL,
  TIME_ID DATE NOT NULL,
  CHANNEL_ID NUMBER NOT NULL,
  PROMO_ID NUMBER NOT NULL,
  QUANTITY_SOLD NUMBER(10,2) NOT NULL,
  AMOUNT_SOLD NUMBER(12,2) NOT NULL,
  CONSTRAINT SH_DEMO_SALES_PK PRIMARY KEY (PROD_ID, CUST_ID, TIME_ID, CHANNEL_ID, PROMO_ID),
  CONSTRAINT SH_DEMO_SALES_PRODUCT_FK FOREIGN KEY (PROD_ID) REFERENCES SH_DEMO.PRODUCTS (PROD_ID),
  CONSTRAINT SH_DEMO_SALES_CUSTOMER_FK FOREIGN KEY (CUST_ID) REFERENCES SH_DEMO.CUSTOMERS (CUST_ID),
  CONSTRAINT SH_DEMO_SALES_TIME_FK FOREIGN KEY (TIME_ID) REFERENCES SH_DEMO.TIMES (TIME_ID),
  CONSTRAINT SH_DEMO_SALES_CHANNEL_FK FOREIGN KEY (CHANNEL_ID) REFERENCES SH_DEMO.CHANNELS (CHANNEL_ID),
  CONSTRAINT SH_DEMO_SALES_PROMO_FK FOREIGN KEY (PROMO_ID) REFERENCES SH_DEMO.PROMOTIONS (PROMO_ID)
);

CREATE TABLE SH_DEMO.COSTS (
  PROD_ID NUMBER NOT NULL,
  TIME_ID DATE NOT NULL,
  UNIT_COST NUMBER(10,2) NOT NULL,
  UNIT_PRICE NUMBER(10,2) NOT NULL,
  CONSTRAINT SH_DEMO_COSTS_PK PRIMARY KEY (PROD_ID, TIME_ID),
  CONSTRAINT SH_DEMO_COSTS_PRODUCT_FK FOREIGN KEY (PROD_ID) REFERENCES SH_DEMO.PRODUCTS (PROD_ID),
  CONSTRAINT SH_DEMO_COSTS_TIME_FK FOREIGN KEY (TIME_ID) REFERENCES SH_DEMO.TIMES (TIME_ID)
);

